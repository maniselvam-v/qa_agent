class GeneratedTestFile(BaseModel):
    req_id: str = Field(description="Requirement ID this file covers")
    filename: str = Field(description="e.g. test_REQ_001_user_auth.py")
    code: str = Field(description="Complete, executable pytest file as a string")
    frameworks_used: list[str] = Field(
        description="e.g. ['httpx', 'playwright', 'databricks-sql-connector']"
    )


class CoderResult(BaseModel):
    test_files: list[GeneratedTestFile] = Field(
        description="One generated test file per requirement"
    )


